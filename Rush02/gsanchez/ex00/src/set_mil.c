#include "../include/rush.h"

char *set_mil(int n)
{
	int i;
	char *mil = malloc(sizeof(char) * (3 * n * 1));
	mil[0] = '1';
	i = 1;
	while (i < (3 * n + 1))
		mil[i++] = '0';
	return (mil);
}