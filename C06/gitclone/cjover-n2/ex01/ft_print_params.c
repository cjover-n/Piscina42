#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int a;

	i = 0;
	a = 1;
	while (a < argc)
	{
		while (argv[a][i] != '\0')
		{
			write(1, &argv[a][i], 1);
			i++;
		}
		write(1, "\n", 1);
		i = 0;
		a++;
	}
	return (0);
}
